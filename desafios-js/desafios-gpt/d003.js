//  Objetivo: Contar a quantidade de caracteres em uma string.

//  Enunciado: Crie uma função chamada contarCaracteres que recebe uma string e retorna o número de caracteres presentes nela.

function contarCaracteres(a) {
    return a.length
}

console.log(contarCaracteres("Hello")) // Deve retornar 5
console.log(contarCaracteres("JavaScript")) // Deve retornar 10